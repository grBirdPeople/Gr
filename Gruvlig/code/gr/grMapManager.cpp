// GetMap
//////////////////////////////////////////////////
grMap*
grMapManager::GetMap( const str& uniqueName )
{
	for( auto& i : m_pVecMap )
	{
		if( i->GetMapData()->Name == uniqueName )
		{
			return i;
		}
	}

	return nullptr;
}


